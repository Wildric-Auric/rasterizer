#define RAS_MATH_IMPL
#include "ras_math.h"
#include <math.h>

double ras_cos(double x) {
    return cos(x);
}

double ras_sin(double x) {
    return sin(x);
}

double ras_tan(double x) {
    return tan(x);
}

double ras_sqrt(double x) {
    return sqrt(x);
}

double ras_pow(double x, double p) {
    return pow(x, p);
}
